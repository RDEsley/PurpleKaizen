create or replace view public.dashboard_metrics
with (security_invoker = true)
as
select
  auth.uid() as owner_id,
  (select count(*) from public.clients c where c.owner_id = auth.uid() and c.status = 'active')::int as active_clients,
  (select count(*) from public.clients c where c.owner_id = auth.uid() and c.status = 'lead')::int as leads,
  (select count(*) from public.tasks t where t.owner_id = auth.uid() and t.status = 'pending')::int as pending_tasks,
  coalesce((
    select sum(case when f.entry_type = 'income' then f.amount else 0 end)
    from public.financial_entries f
    where f.owner_id = auth.uid()
      and date_trunc('month', f.occurred_on) = date_trunc('month', now())
  ), 0)::numeric(12,2) as monthly_income,
  coalesce((
    select sum(case when f.entry_type = 'expense' then f.amount else 0 end)
    from public.financial_entries f
    where f.owner_id = auth.uid()
      and date_trunc('month', f.occurred_on) = date_trunc('month', now())
  ), 0)::numeric(12,2) as monthly_expense;

grant select on public.dashboard_metrics to authenticated;
